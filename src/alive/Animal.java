package alive;

public class Animal {

    int age;
    String gender;
    String name;
    int height;
    int kilo;
    boolean isWild;
    String voice;
    boolean isteeth;

    //Constructor


    //behavioral




    //getter & setter
    public boolean isIsteeth() {
        return isteeth;
    }

    public void setIsteeth(boolean isteeth) {
        this.isteeth = isteeth;
    }

    public int getAge() {
        return age;

    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

    public int getKilo() {
        return kilo;
    }

    public void setKilo(int kilo) {
        this.kilo = kilo;
    }

    public boolean isWild() {
        return isWild;
    }

    public void setWild(boolean wild) {
        isWild = wild;
    }

    public String getVoice() {
        return voice;
    }

    public void setVoice(String voice) {
        this.voice = voice;
    }
}
